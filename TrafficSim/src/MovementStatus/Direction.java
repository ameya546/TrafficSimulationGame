package MovementStatus;

/**
 * Defines the cardinal directions as an enumeration for use in specifying orientation
 * or movement directions within the application. This can be particularly useful in navigation,
 * traffic management, or any scenario where a clear, cardinal directional context is required.
 */
public enum Direction {
    /**
     * Represents the cardinal north direction.
     */
    NORTH,

    /**
     * Represents the cardinal south direction.
     */
    SOUTH,

    /**
     * Represents the cardinal west direction.
     */
    WEST,

    /**
     * Represents the cardinal east direction.
     */
    EAST
}
